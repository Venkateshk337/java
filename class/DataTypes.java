
public class DataTypes {
    public static void main(String[] arg){
        int in=10;
        float flo=20.1f;
        double dou=30.568;
        boolean bool=true;
        short sho=10;
        char cha='#';
        long lo=2004158;
        byte byt=3;
        System.out.println("int:"+in);
        System.out.println("float:"+flo);
        System.out.println("double:"+dou);
        System.out.println("boolean:"+bool);
        System.out.println("short:"+sho);
        System.out.println("long:"+cha);
        System.out.println("byte:"+lo);
        System.out.println("char:"+byt);
    }
}
